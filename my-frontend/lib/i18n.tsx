type PreferencesContextValue = {
  language: Language;
  currency: Currency;
  setLanguage: (language: Language) => void;
  setCurrency: (currency: Currency) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  formatPrice: (amountUsd: number) => string;
  convertPrice: (amountUsd: number) => number;
  translateCategory: (name: string) => string;
  translateDbText: (text: string | undefined | null) => string;
};

const PreferencesContext = createContext<PreferencesContextValue | undefined>(
  undefined,
);

export function PreferencesProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(DEFAULT_LANGUAGE);
  const [currency, setCurrencyState] = useState<Currency>(DEFAULT_CURRENCY);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }
    
    const syncPreferences = () => {
      const storedLanguage = window.localStorage.getItem(
        "preferred-language",
      ) as Language | null;
      const storedCurrency = window.localStorage.getItem(
        "preferred-currency",
      ) as Currency | null;

      if (storedLanguage === "en" || storedLanguage === "vi") {
        setLanguageState(storedLanguage);
      }
      if (storedCurrency === "USD" || storedCurrency === "VND") {
        setCurrencyState(storedCurrency);
      }
    };

    syncPreferences();

    window.addEventListener("storage", syncPreferences);
    return () => {
      window.removeEventListener("storage", syncPreferences);
    };
  }, []);

  useEffect(() => {
    if (typeof window === "undefined") {
      return;
    }
    
    const currentLang = window.localStorage.getItem("preferred-language");
    const currentCurr = window.localStorage.getItem("preferred-currency");

    if (currentLang !== language || currentCurr !== currency) {
      window.localStorage.setItem("preferred-language", language);
      window.localStorage.setItem("preferred-currency", currency);
      document.documentElement.lang = language;
      window.dispatchEvent(new Event("storage"));
    }
  }, [language, currency]);

  const value = useMemo<PreferencesContextValue>(() => {
    return {
      language,
      currency,
      setLanguage: setLanguageState,
      setCurrency: setCurrencyState,
      t: (key, params) => translate(key, language, params),
      formatPrice: (amountUsd) => formatPrice(amountUsd),
      convertPrice: (amountUsd) => convertPrice(amountUsd),
      translateCategory: (name) => translateCategory(name, language),
      translateDbText: (text) => translateDbText(text, language),
    };
  }, [language, currency]);

  return (
    <PreferencesContext.Provider value={value}>
      {children}
    </PreferencesContext.Provider>
  );
}

export function usePreferences(): PreferencesContextValue {
  const context = useContext(PreferencesContext);
  if (!context) {
    throw new Error("usePreferences must be used within PreferencesProvider");
  }
  return context;
}
